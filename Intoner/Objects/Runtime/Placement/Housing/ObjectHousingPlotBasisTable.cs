    public static bool TryResolve(ObjectHousingDistrict district, int plot, byte blockId, out ObjectHousingPlotBasis basis)
    {
        basis = null!;
        PlotBasisData[] table = SelectDistrictTable(district);
        if (plot < 1 || plot > table.Length)
        {
            return false;
        }

        PlotBasisData data = table[plot - 1];
        basis = new ObjectHousingPlotBasis(
            district,
            plot,
            blockId,
            ObjectHousingAddress.GetSize(district, plot),
            new Vector3(data.X, data.Y, data.Z),
            data.RotationRadians);
        return true;
    }

